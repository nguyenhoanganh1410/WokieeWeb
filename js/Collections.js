function renderShopping(name,value){
  $(".shoppingBy").empty();
  const uniqueFillterName = name.filter((val,idx) =>{
    return idx === name.findIndex(v => val === v)
  }) 
  
  uniqueFillterName.forEach((val,idx) =>{

    $(".shoppingBy").append(`
    <li><span style="font-sixe:12px">${val.toUpperCase()}</span></li>
    `)
    name.forEach((v, pos) =>{
      if(val.toLowerCase() == v.toLowerCase()){
        $(".shoppingBy").append(`
        <li class="shoppingBy-item" data-id="${value[pos]}" style="margin:0px"><span class="remove__fillter">x</span><a><span class="value-fillter" class="${radomData_id()}">${value[pos]}</span></a></li>
        `)
      }
    })
  })
}

