//render star
function renderStar(list) {
  list.forEach((val) => {
    $(`.${val.id}`).empty();
  });
  list.forEach((val) => {
    for (let i = 0; i < val.evaluate; i++) {
      $(`.${val.id}`).append(`
               <i class="fas fa-star starActive"></i>
         `);
    }
  });
  list.forEach((val) => {
    for (let i = 0; i < 5 - val.evaluate; i++) {
      $(`.${val.id}`).append(`
      <i class="far fa-star"></i>
      `);
    }
  });
  
}

