function showProduct(list){
    var fillList = [...list];//sao chep ds san pham goc
    
    FillterName.forEach((val,idx) =>{
      //th1: vao vender
      if(val.toLowerCase() == "vendor"){
        fillList = fillList.filter(product =>{
            return product.brand.toLowerCase() == FillterValue[idx].toLowerCase()
          })
        // console.log(fillList);
          //render len sp
          renderProducts(fillList, ".itemList");
      }
      else if(val.toLowerCase() == "producttype"){
        fillList = fillList.filter(product =>{
          return product.productType.toLowerCase() == FillterValue[idx].toLowerCase()
        })
      // console.log(fillList);
        //render len sp
        renderProducts(fillList, ".itemList");
      }
      else if(val.toLowerCase() == "price"){  
        let priceList = changeMoney(FillterValue[idx])   
        console.log(priceList); 
        fillList = fillList.filter(product =>{
            return TinhTienSort(product.price, product.sale) >= priceList[0] && TinhTienSort(product.price, product.sale) <= priceList[1]
        })

        renderProducts(fillList, ".itemList");
      }

    })
}




